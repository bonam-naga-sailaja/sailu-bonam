package javademo;

public class operators 
{
	

	public static void main(String[] args)  
	{
	int a = 2;
	int b=4;
	int c=a+b;
	System.out.println("the value of c is:"+c);
	int x= ++a;
	System.out.println(x);
	int y= b++;
	System.out.println(y);
	int z= --a;
	System.out.println(z);
	int v= a--;
	System.out.println(v);
	}
}
	
	
	
	

	


	


